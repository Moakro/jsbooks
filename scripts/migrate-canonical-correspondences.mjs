#!/usr/bin/env node
/**
 * 임시판 천지개벽경 anchor 기준 correspondences를 정식판 한자 anchor로 이행.
 *
 * 입력:
 *   - 매핑 JSON: content/scripture/_mappings/cheonjigaebyeokgyeong-canonical.json
 *     (정식판 한자 anchor → 매핑된 한글 anchor[])
 *   - correspondences.yml: 임시판 anchor가 source인 entries
 *
 * 출력:
 *   - 같은 yml에 정식판 anchor source 신규 entries 추가 (origin: curator → AI rebuild에도 보존)
 *   - 같은 dir의 correspondences.json도 함께 갱신
 *
 * 멱등성: 이미 존재하는 정식판 entry는 건너뜀.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const MAPPING_PATH = path.join(
  ROOT,
  "content/scripture/_mappings/cheonjigaebyeokgyeong-canonical.json",
);
const YML_PATH = path.join(ROOT, "content/_data/correspondences.yml");
const JSON_PATH = path.join(ROOT, "content/_data/correspondences.json");

// ── 1. 매핑 JSON → 한글 anchor 역인덱스 ────────────────────────────────────
const mapping = JSON.parse(fs.readFileSync(MAPPING_PATH, "utf8"));
const hangeulToHanja = new Map(); // "1-1-3" → "1-1-3" (canonical hanja)
for (const [hanjaAnchor, v] of Object.entries(mapping.verses)) {
  if (!Array.isArray(v.hangeul)) continue;
  for (const h of v.hangeul) {
    if (hangeulToHanja.has(h)) {
      console.warn(
        `[warn] hangeul anchor ${h} appears in multiple hanja mappings (${hangeulToHanja.get(h)}, ${hanjaAnchor}); using first`,
      );
      continue;
    }
    hangeulToHanja.set(h, hanjaAnchor);
  }
}
console.log(`reverse index built: ${hangeulToHanja.size} hangeul anchors`);

// ── 2. yml 단순 파서 (이 스키마 전용) ──────────────────────────────────────
const ymlText = fs.readFileSync(YML_PATH, "utf8");
const headerLines = [];
const entries = []; // { source, matches: [{target, id, similarity?, origin, status, note?}] }
let cur = null;
for (const rawLine of ymlText.split("\n")) {
  const line = rawLine;
  if (line.startsWith("- source:")) {
    if (cur) entries.push(cur);
    cur = { source: line.slice("- source:".length).trim(), matches: [] };
  } else if (line.trim().startsWith("matches:")) {
    // skip
  } else if (line.trim().startsWith("- target:")) {
    cur.matches.push({ target: line.trim().slice("- target:".length).trim() });
  } else if (cur && cur.matches.length > 0 && /^\s+\w+:\s/.test(line)) {
    const m = line.trim().match(/^(\w+):\s*(.*)$/);
    if (m) {
      let v = m[2];
      if (v.startsWith('"') && v.endsWith('"')) v = JSON.parse(v);
      else if (/^-?\d+(\.\d+)?$/.test(v)) v = parseFloat(v);
      cur.matches[cur.matches.length - 1][m[1]] = v;
    }
  } else if (line.startsWith("#") || line.trim() === "") {
    if (entries.length === 0 && !cur) headerLines.push(line);
  }
}
if (cur) entries.push(cur);
console.log(`parsed ${entries.length} entries from yml`);

// ── 3. 임시판 → 정식판 entries 생성 ────────────────────────────────────────
const existingSources = new Set(entries.map((e) => e.source));
let migrated = 0;
let skippedExisting = 0;
let skippedNoMap = 0;

for (const e of entries) {
  if (!e.source.startsWith("cheonjigaebyeokgyeong#")) continue;
  const tempAnchor = e.source.split("#")[1];
  const hanjaAnchor = hangeulToHanja.get(tempAnchor);
  if (!hanjaAnchor) {
    skippedNoMap++;
    continue;
  }
  const newSource = `cheonjigaebyeokgyeong-canonical#${hanjaAnchor}`;
  if (existingSources.has(newSource)) {
    skippedExisting++;
    continue;
  }
  const newMatches = e.matches.map((m) => ({
    ...m,
    id: `cheonjigaebyeokgyeong-canonical-${hanjaAnchor}__${m.target.replace("#", "-")}`,
    origin: "curator", // AI rebuild에 영향받지 않도록
  }));
  entries.push({ source: newSource, matches: newMatches });
  existingSources.add(newSource);
  migrated++;
}
console.log(
  `migrated ${migrated} entries to canonical (skipped existing ${skippedExisting}, no-mapping ${skippedNoMap})`,
);

// ── 4. yml 재생성 ──────────────────────────────────────────────────────────
const lines_out = [
  "# Auto-generated by scripts/build-correspondences.ts",
  "# DO NOT delete entries with origin: curator or origin: community.",
  "# AI-origin entries with status != active are also preserved on rebuild.",
  `# Threshold: 0.7   Generated: ${new Date().toISOString()}`,
  "",
];
entries.sort((a, b) => a.source.localeCompare(b.source));
for (const e of entries) {
  lines_out.push(`- source: ${e.source}`);
  lines_out.push(`  matches:`);
  for (const m of e.matches) {
    lines_out.push(`    - target: ${m.target}`);
    if (m.id) lines_out.push(`      id: ${m.id}`);
    if (typeof m.similarity === "number")
      lines_out.push(`      similarity: ${m.similarity}`);
    if (m.origin) lines_out.push(`      origin: ${m.origin}`);
    if (m.status) lines_out.push(`      status: ${m.status}`);
    if (m.note) lines_out.push(`      note: ${JSON.stringify(m.note)}`);
  }
  lines_out.push("");
}
fs.writeFileSync(YML_PATH, lines_out.join("\n"));
console.log(`wrote ${entries.length} entries → ${YML_PATH}`);

// ── 5. json runtime map 재생성 ────────────────────────────────────────────
const jsonMap = {};
function pushBoth(source, m) {
  const [s_slug, s_anchor] = source.split("#");
  const [t_slug, t_anchor] = m.target.split("#");
  if (!s_slug || !s_anchor || !t_slug || !t_anchor) return;
  const status = m.status ?? "active";
  if (status === "hidden") return;
  const id = m.id ?? `${s_slug}-${s_anchor}__${t_slug}-${t_anchor}`;
  const fwd = {
    id,
    source,
    target: m.target,
    similarity: m.similarity,
    origin: m.origin ?? "ai",
    status,
    note: m.note,
    source_slug: s_slug,
    source_anchor: s_anchor,
    target_slug: t_slug,
    target_anchor: t_anchor,
  };
  (jsonMap[source] ??= []).push(fwd);
  (jsonMap[m.target] ??= []).push({
    ...fwd,
    source: m.target,
    target: source,
    source_slug: t_slug,
    source_anchor: t_anchor,
    target_slug: s_slug,
    target_anchor: s_anchor,
  });
}
for (const e of entries) for (const m of e.matches) pushBoth(e.source, m);
fs.writeFileSync(JSON_PATH, JSON.stringify(jsonMap));
console.log(`wrote runtime json → ${JSON_PATH}`);
